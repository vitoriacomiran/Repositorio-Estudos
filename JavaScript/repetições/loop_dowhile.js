// DO WHILE: repete até que a condição especificada seja verdadeira.
// No entanto a instrução será executada pelo menos uma vez antes da condição ser verdadeira


let value = 0

do {
    value++
    console.log(value)
} while (value < 10)

console.log("Segue fluxo...")

// faz a veriificação no final
// enquanto o valor for menor que 10 a repetição continua

