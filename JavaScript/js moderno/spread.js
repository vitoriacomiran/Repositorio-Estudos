// espalhar => permite que um objeto iterável, array ou string, seja expandido para ser usado
// onde zero ou mais argumentos

const numbers = [1, 2, 3]
console.log(...numbers)

// retorna (3) [1,2,3]

const names = ["Ana", "Maria", "João"]
console.log(...names)

// retorna (3) ["Ana", "Maria", "João"]