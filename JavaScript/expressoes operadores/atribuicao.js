let value

value = 10;
// atribuição simples
console.log(value); // 10

value += 5; // atribuição com adição
console.log(value); // 15

value -= 3; // atribuição com subtração
console.log(value); // 12

value *= 2; // atribuição com multiplicação
console.log(value); // 24

value /= 4; // atribuição com divisão
console.log(value); // 6

value %= 5; // atribuição com resto da divisão
console.log(value); // 1

value **= 3; // atribuição com exponenciação
console.log(value); // 1, pois 1 elevado a qualquer potência é 1

